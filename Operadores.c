#include<stdio.h>
int main ()
{
	// declaracion de variables 
	int a=5;
	int b=7;
	// operaciones o codigo
	
	printf ("(a==b)&&(a<b) el resultado es: %d\n",(a==b)&&(a<b));
	printf ("(a==5)||(a>7) el resultado es: %d\n",(a==5)||(a>7));
	printf ("!(a==5) el resultado es: %d \n",!(a==5));
	return(0);
}
